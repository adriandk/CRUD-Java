    private void tabelpelangganMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelpelangganMouseClicked
        int rowIndex = tabelpelanggan.getSelectedRow();

        kodepelanggan.setText((String) tabelpelanggan.getValueAt(rowIndex, 0));
        int kodepel = Integer.parseInt(kodepelanggan.getText());

        try {
            konek.query = "select * from pelanggan where kode_pelanggan = '" + kodepel + "'";
            konek.getData();
            if (konek.result.next()) {
                kode = konek.result.getString(1);
                nama = konek.result.getString(2);
                alamatpel = konek.result.getString(3);
                nohppel = konek.result.getString(4);
            }

        } catch (SQLException ex) {

        }

        namapelanggan.setText(nama);
        alamat.setText(alamatpel);
        nohp.setText(nohppel);
    }//GEN-LAST:event_tabelpelangganMouseClicked

