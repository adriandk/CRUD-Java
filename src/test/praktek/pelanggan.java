    private void hapusMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_hapusMouseClicked
        int a = JOptionPane.showConfirmDialog(null, "Kamu yakin mau menghapus data " + namapelanggan.getText() + " ?", "Waring", JOptionPane.YES_NO_OPTION);

        int kodepel = Integer.parseInt(kodepelanggan.getText());

        if (a == JOptionPane.YES_OPTION) {
            try {
                konek.query = "delete from pelanggan where kode_pelanggan = '" + kodepel + "'";
                konek.setData();
                konek.preparedstatement.executeUpdate();
                clear();
                data();
            } catch (SQLException ex) {
            }
        } else {

        }
    }//GEN-LAST:event_hapusMouseClicked

    private void editMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editMouseClicked
        int kodepel = Integer.parseInt(kodepelanggan.getText());
        if (kodepelanggan.getText().equals("") || namapelanggan.getText().equals("") || alamat.getText().equals("") || nohp.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "belum benar", "info", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                konek.query = "update pelanggan set kode_pelanggan = ?, nama_pelanggan = ?, alamat = ?, nohp = ? where kode_pelanggan = '" + kodepel + "'";
                konek.setData();
                konek.preparedstatement.setInt(1, Integer.parseInt(kodepelanggan.getText()));
                konek.preparedstatement.setString(2, namapelanggan.getText());
                konek.preparedstatement.setString(3, alamat.getText());
                konek.preparedstatement.setString(4, nohp.getText());

                konek.preparedstatement.executeUpdate();
                data();
                clear();
                kode();
            } catch (SQLException ex) {
                Logger.getLogger(pelanggan.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_editMouseClicked

