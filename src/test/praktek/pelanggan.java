    void kode() {
        try {
            konek.query = "select MAX(right(kode_pelanggan, 1)) as kode from pelanggan";
            konek.getData();
            while (konek.result.next()) {
                if (konek.result.first() == false) {
                    kodepelanggan.setText("1");
                } else {
                    konek.result.last();

                    int id = konek.result.getInt(1) + 1;

                    String no = String.valueOf(id);

                    kodepelanggan.setText(no);
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(pelanggan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    void data() {
        try {
            tabModel = (DefaultTableModel) tabelpelanggan.getModel();
            tabModel.getDataVector().removeAllElements();
            revalidate();

            konek.query = "select * from pelanggan";
            konek.getData();

            while (konek.result.next()) {
                kode = konek.result.getString(1);
                nama = konek.result.getString(2);
                alamatpel = konek.result.getString(3);
                nohppel = konek.result.getString(4);

                String[] data = {kode, nama, alamatpel, nohppel};
                tabModel.addRow(data);
            }
        } catch (SQLException ex) {

        }
    }

